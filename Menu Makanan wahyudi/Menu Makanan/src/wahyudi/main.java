    private void hitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hitungActionPerformed
        // TODO add your handling code here:
        boolean data = true;
        
        if(soto.isSelected() && "".equals(nilai1.getText())) data=false;
        if(bakso.isSelected() && "".equals(nilai2.getText())) data=false;
        if(jeruk.isSelected() && "".equals(nilai3.getText())) data=false;
        if(teh.isSelected() && "".equals(nilai4.getText())) data=false;
        
        if(!data){
            JOptionPane.showMessageDialog(rootPane,"Data Jumlah Pembelian salah input");
        }else{
            int soto = Integer.parseInt(nilai1.getText());
            int bakso = Integer.parseInt(nilai2.getText());
            int jeruk = Integer.parseInt(nilai3.getText());
            int teh = Integer.parseInt(nilai4.getText());
            int jumlah = (soto*5000)+(bakso*10000)+(jeruk*2000)+(teh*1500);
            
            bayar.setText("Rp."+Integer.toString(jumlah));
        }
    }//GEN-LAST:event_hitungActionPerformed

